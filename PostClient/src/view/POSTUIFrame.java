    /**
     * Display the POST (items) for the current transaction
     *
     */
    public String displayPOST() throws RemoteException {
        Customer customer = new Customer();
        String name = nameField.getText();
        StringTokenizer tok = new StringTokenizer(name);
        //parse customer name into first and last
        try {
            customer.setFirstName(tok.nextToken());
            if (tok.hasMoreTokens()) {
                customer.setLastName(tok.nextToken());
            }
        } catch (NoSuchElementException ex) {
            String error = "\nError: The customer name is empty!";
            return output + error;
        }
        if (transaction == null) {
            transaction = post.createTransaction();
            transaction.setCustomer(customer);
            jLabel13.setText(transaction.getDateAndTime().toString());
        }
        Item item = new Item();
        item.setProductSpec(store.getProduct(jComboBox1.getSelectedItem().toString()));
        item.setQuantity(Integer.valueOf(jComboBox2.getSelectedItem().toString()));
        if (!jComboBox2.getSelectedItem().toString().equals("0")) {
            if (!transaction.hasItem(item)) {
                transaction.addItem(item);
            } else {
                transaction.updateItem(item);
            }
        } else {
            transaction.removeItem(item);
        }
        output = "";
        ArrayList<Item> transItems = transaction.getItems();
        for (Item transItem : transItems) {
            double price = store.getProduct(transItem.getUPC()).getPrice();
            output += String.format("%-50s\t %25s %30.2f %40.2f\n",
                    store.getProduct(transItem.getUPC()).getDescription(),
                    transItem.getQuantity(), price, price * transItem.getQuantity());
        }
        try {
            TotalLabel.setText("$" + String.valueOf(post.getTotal(transaction)));
        } catch (Exception ex) {
            System.err.println(ex);
        }

        return output;
    }

